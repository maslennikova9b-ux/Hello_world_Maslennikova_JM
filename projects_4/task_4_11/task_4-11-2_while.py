# Нахождение скалярного произведения двух векторов с помощью цикла while

# Задаём два вектора одинаковой длины
a = [1, 2, 3]
b = [4, 5, 6]

# Определяем размер вектора (длину)
n = len(a)

# Инициализация переменных
i = 0        # счётчик (индекс)
scalar = 0   # сумма скалярного произведения

# Цикл while выполняется, пока индекс меньше длины вектора
while i < n:
    # Умножаем соответствующие элементы и добавляем к сумме
    scalar = scalar + a[i] * b[i]
    # Увеличиваем счётчик (важно! иначе бесконечный цикл)
    i = i + 1

# Вывод результата
print("Скалярное произведение векторов", a, "и", b, ":", scalar)