# Нахождение скалярного произведения двух векторов с помощью цикла for

# Задаём два вектора одинаковой длины
a = [1, 2, 3]
b = [4, 5, 6]

# Определяем размер вектора (длину)
n = len(a)

# Инициализация переменной для хранения скалярного произведения
scalar = 0

# Цикл for перебирает индексы от 0 до n-1
for i in range(n):
    # Умножаем соответствующие элементы и добавляем к сумме
    scalar = scalar + a[i] * b[i]

# Вывод результата
print("Скалярное произведение векторов", a, "и", b, ":", scalar)