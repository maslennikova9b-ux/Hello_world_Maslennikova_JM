# Сортировка произвольного массива по возрастанию
# Алгоритм: сортировка пузырьком (Bubble Sort)

# Исходный массив
array = [7, 3, 8, 1, 4, 6, 2, 5]

print("Исходный массив:", array)

# Определяем длину массива
n = len(array)

# Внешний цикл: количество проходов
for i in range(n):
    # Внутренний цикл: сравнение соседних элементов
    # n - i - 1 — оптимизация: после каждого прохода
    # самый большой элемент "всплывает" в конец
    for j in range(n - i - 1):
        # Если текущий элемент больше следующего, меняем их местами
        if array[j] > array[j + 1]:
            # Обмен значениями без использования временной переменной
            array[j], array[j + 1] = array[j + 1], array[j]

# Вывод отсортированного массива
print("Отсортированный массив:", array)