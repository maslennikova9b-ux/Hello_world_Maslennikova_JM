# Нахождение среднего арифметического из 4-х чисел

# Четыре числа
a = 10
b = 20
c = 30
d = 40

print(f"Числа: {a}, {b}, {c}, {d}")

# Вычисление суммы
sum_numbers = a + b + c + d

# Вычисление среднего арифметического
average = sum_numbers / 4

# Вывод результата
print(f"Сумма: {sum_numbers}")
print(f"Среднее арифметическое: {average}")