# Нахождение суммы квадратов первых N чисел

N = 6  # можно изменить на любое число

print(f"Сумма квадратов первых {N} натуральных чисел")

# Вычисление суммы квадратов
sum_squares = 0
for i in range(1, N + 1):
    sum_squares += i ** 2
    print(f"{i}² = {i**2}, сумма квадратов пока: {sum_squares}")

# Вывод результата
print(f"1² + 2² + ... + {N}² = {sum_squares}")