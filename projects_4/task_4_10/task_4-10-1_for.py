# Нахождение суммы всех чётных чисел от 1 до 15 с помощью цикла for

# Инициализация переменной для хранения суммы
sum_even = 0

# Цикл for перебирает числа от 1 до 15
for i in range(1, 16):
    # Проверка на чётность
    if i % 2 == 0:
        # Добавление чётного числа к сумме
        sum_even += i

# Вывод результата
print("Сумма всех чётных чисел от 1 до 15:", sum_even)