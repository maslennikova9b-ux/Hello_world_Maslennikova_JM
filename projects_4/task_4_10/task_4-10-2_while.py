# Нахождение суммы всех чётных чисел от 1 до 15 с помощью цикла while

# Инициализация переменных
i = 1           # счётчик
sum_even = 0    # сумма чётных чисел

# Цикл while выполняется, пока i меньше 16
while i < 16:
    # Проверка на чётность
    if i % 2 == 0:
        # Добавление чётного числа к сумме
        sum_even += i
    # Увеличение счётчика (важно! без этого будет бесконечный цикл)
    i += 1

# Вывод результата
print("Сумма всех чётных чисел от 1 до 15:", sum_even)