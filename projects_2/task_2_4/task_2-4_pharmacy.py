# Задаем значения напрямую
total_capsules = 220  # Общее количество произведенных капсул
pack_size = 45        # Вместимость одной упаковки

# Рассчитываем количество полных упаковок и остаток
# // - целочисленное деление (сколько целых упаковок)
# % - остаток от деления (сколько капсул останется)
full_packs = total_capsules // pack_size
remaining_capsules = total_capsules % pack_size

# Выводим отчет с использованием табуляции (\t) для отступов
print("\n--- Отчет фасовочного цеха ---")
print(f"Полных упаковок:\t{full_packs}")
print(f"Остаток капсул:\t\t{remaining_capsules}")