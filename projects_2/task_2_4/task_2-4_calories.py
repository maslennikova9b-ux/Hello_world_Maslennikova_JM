# Задаем значения напрямую
proteins = 10  # Белки в граммах
fats = 15      # Жиры в граммах
carbohydrates = 10  # Углеводы в граммах

# Рассчитываем общую калорийность
# Калорийность белков и углеводов = 4 ккал/г, жиров = 9 ккал/г
calories = (proteins * 4) + (fats * 9) + (carbohydrates * 4)

# Выводим результат с помощью f-строки
print(f"Общая калорийность продукта: {calories:.2f} ккал")