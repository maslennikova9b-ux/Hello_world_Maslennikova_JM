# Задаем нужный объем раствора
volume = 250  # Объем физиологического раствора в мл

# Рассчитываем массу соли для 0.9% концентрации
# Формула: масса = объем * 0.009
salt_mass = volume * 0.009

# Объем воды считаем равным общему объему
# (считаем, что объем соли пренебрежимо мал)
water_volume = volume

# Открываем файл для записи рецепта
with open("recipe.txt", "w", encoding="utf-8") as file:
    # Записываем отчет в файл
    file.write("ОТЧЕТ ПО ПРИГОТОВЛЕНИЮ:\n")
    file.write("-" * 23 + "\n")  # Разделительная линия
    file.write(f"Общий объем:\t{volume:.2f} мл\n")
    file.write(f"Масса соли:\t{salt_mass:.2f} г\n")
    file.write(f"Объем воды:\t{water_volume:.2f} мл\n")

# Выводим сообщение об успешном создании файла
print(f"\nРецепт успешно сохранен в файл recipe.txt")
print(f"Для объема {volume:.2f} мл потребуется {salt_mass:.2f} г соли и {water_volume:.2f} мл воды")