# task_2-4_bmi.py

# Задаем значения напрямую
weight = 52  # Вес в килограммах
height_cm = 155  # Рост в сантиметрах

# Переводим рост из сантиметров в метры для формулы ИМТ
height_m = height_cm / 100

# Рассчитываем индекс массы тела (ИМТ)
# Формула: вес (кг) / рост (м)^2
bmi = weight / (height_m ** 2)

# Выводим отчет с использованием табуляции (\t) для отступов
print("\n--- Отчет о состоянии здоровья ---")
print(f"Рост:\t\t{height_cm:.1f} см")
print(f"Вес:\t\t{weight:.1f} кг")
print(f"Индекс массы тела:\t{bmi:.2f}")